        loader=LoadHF(path="clinc_oos", name=subset),
        preprocess_steps=[
            Shuffle(page_size=sys.maxsize),
            Rename(field_to_field={"intent": "label"}),
            MapInstanceValues(mappers={"label": map_label_to_text}),
            Set(
                fields={
                    "classes": classes,
                    "text_type": "sentence",
                    "type_of_class": "intent",
                }
            ),
        ],
